import * as React from "react"

import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & {
    blurBackground?: boolean
  }
>(({ className, blurBackground = false, children, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-md border border-neutral-75 bg-card text-card-foreground shadow-sm",
      className,
      blurBackground && "relative bg-transparent"
    )}
    {...props}
  >
    {blurBackground && (
      <div className="pointer-events-none absolute left-0 top-0 z-0 size-full bg-background opacity-[0.01]" />
    )}
    {children}
  </div>
))
Card.displayName = "Card"

